let estação = 2;

switch(estação){
    case 1:
        console.log(`Verão`); 
        break;
    case 2:
        console.log(`Primavera`); 
        break;
    case 3:
        console.log(`Outono`); 
        break;
    case 4:
        console.log(`Inverno`); 
        break;
default:
    console.log(`Estação Inválida!`)
}
